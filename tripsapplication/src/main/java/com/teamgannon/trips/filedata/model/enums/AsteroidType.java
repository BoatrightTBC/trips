package com.teamgannon.trips.filedata.model.enums;

/**
 * the asteroid type
 * <p>
 * Asteroid Spectroscopic Survey (SMASS) of 1,447 asteroids.[4] This survey produced spectra of a far higher
 * resolution than ECAS, and was able to resolve a variety of narrow spectral features. However, a somewhat
 * smaller range of wavelengths (0.44 μm to 0.92 μm) was observed. Also, albedos were not considered. Attempting
 * to keep to the Tholen taxonomy as much as possible given the differing data, asteroids were sorted into the
 * 26 types given below. The majority of bodies fall again into the three broad C, S, and X categories, with a
 * few unusual bodies categorized into several smaller types
 * <p>
 * C-group of carbonaceous objects including:[citation needed]
 * B-type largely overlapping with the Tholen B and F types.
 * C-type the most 'standard' of the non-B carbonaceous objects
 * Cg Ch Cgh somewhat related to the Tholen G type
 * Cb transition objects between plain C and B types.
 * S-group of silicaceous (stony) objects including:
 * A-type
 * Q-type
 * R-type
 * K-type a new category (181 Eucharis, 221 Eos)
 * L-type a new category (83 Beatrix)
 * S-type the most 'standard' of the S group
 * Sa, Sq, Sr, Sk, and Sl transition objects between plain S and the other types in the group.
 * X-group of mostly metallic objects including:
 * X-type the most 'standard' of the X group including objects classified by Tholen as M, E, or P-type.
 * Xe, Xc, and Xk transition types between plain X and the appropriately lettered types.
 * T-type
 * D-type
 * Ld-type: a new type with more extreme spectral features than the L-type
 * O-type a small category (3628 Božněmcová)
 * V-type (4 Vesta)
 * A significant number of small asteroids were found to fall in the Q, R, and V types, which were represented by
 * only a single body in the Tholen scheme. In this Bus and Binzel SMASS scheme only a single type was assigned
 * to any particular asteroid.
 * A few near-Earth objects have spectra that differ strongly from any of the SMASS classes. This is presumably
 * because these bodies are much smaller than those detected in the asteroid belt, and as such may have younger
 * less-altered surfaces or be composed of a less varied mix of minerals
 * <p>
 * Created by larrymitchell on 2017-01-24.
 */
public enum AsteroidType {

    B_Type_CGroup,

    C_Type_CGroup,

    A_Type_SGroup,

    Q_Type_SGroup,

    R_Type_SGroup,

    K_Type_SGroup,

    L_Type_SGroup,

    S_Type_SGroup,

    X_Type_XGroup,

    T_Type,

    D_Type,

    Ld_Type,

    O_Type,

    V_Type


}
